    private void tabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel1MouseClicked
    //Klik Tabel
        int baris = tabel1.rowAtPoint(evt.getPoint());
        
        String Id_pembeli = tabel1.getValueAt(baris, 0).toString();
        txt_id.setText(Id_pembeli);
        
        String Nama = tabel1.getValueAt(baris, 1).toString();
        txt_name.setText(Nama);
        
        String Jenis_kelamin = tabel1.getValueAt(baris, 2).toString();
        txt_jeniskelamin.setText(Jenis_kelamin);
        
        String Kode_pos = tabel1.getValueAt(baris, 3).toString();
        txt_kdpos.setText(Kode_pos);
        
        String Alamat = tabel1.getValueAt(baris, 4).toString();
        txt_alamat.setText(Alamat);
        
        String No_hp = tabel1.getValueAt(baris, 5).toString();
        txt_hp.setText(No_hp);
        
        String Email = tabel1.getValueAt(baris, 6).toString();
        txt_email.setText(Email);
    }//GEN-LAST:event_tabel1MouseClicked

