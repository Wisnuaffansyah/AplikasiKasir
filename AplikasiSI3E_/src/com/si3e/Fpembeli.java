    public void tampilkan_data(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id Pembeli");
        model.addColumn("Nama");
        model.addColumn("Jenis Kelamin");
        model.addColumn("Kode Pos");
        model.addColumn("Alamat");
        model.addColumn("No Hp");
        model.addColumn("Email");
        
        String cari = txt_cari.getText();
        try{
            int no = 1; 
            String sql = "Select * From pembeli WHERE Nama LIKE'%"+cari+"%'";
            java.sql.Connection conn=(Connection)Konfig.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while (res.next()){
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6),res.getString(7)});
                        
                tabel1.setModel(model);
            }
        }catch(SQLException e){
            System.out.println("Error :" +e.getMessage());
            
        }
    }
    
