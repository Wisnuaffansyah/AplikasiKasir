    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
    //Edit Data
        try{             
            String sql = "UPDATE detail_barang set kode_brg='"+txt_kdbarang.getText()+"',"
                        +"merk='"+txt_merk.getText()+"',"
                        +"size='"+txt_size.getText()+"',"
                        +"Warna='"+txt_warna.getText()+"'" 
                        +"where id_barang='"+txt_idbarang.getText()+"'";
            java.sql.Connection con = (Connection)Konfig.configDB();
            java.sql.PreparedStatement pst = con.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Edit Data Berhasil");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Edit Data Gagal");
        }
        tampilkan_data();
    }//GEN-LAST:event_btn_updateActionPerformed

