    private void tabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel2MouseClicked
    //Klik Tabel
        int baris = tabel2.rowAtPoint(evt.getPoint());
        
        String kd_brg = tabel2.getValueAt(baris, 0).toString();
        txt_kdbarang.setText(kd_brg);
        
        String id_barang = tabel2.getValueAt(baris, 1).toString();
        txt_idbarang.setText(id_barang);
        
        String Merk = tabel2.getValueAt(baris, 2).toString();
        txt_merk.setText(Merk);
        
        String size = tabel2.getValueAt(baris, 3).toString();
        txt_size.setText(size);
        
        String Warna = tabel2.getValueAt(baris, 4).toString();
        txt_warna.setText(Warna);
    }//GEN-LAST:event_tabel2MouseClicked

